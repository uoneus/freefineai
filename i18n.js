let currentLang = localStorage.getItem('lang') ||
  (navigator.language.startsWith('zh') ? 'zh' :
   navigator.language.startsWith('ru') ? 'ru' :
   navigator.language.startsWith('ja') ? 'ja' :
   navigator.language.startsWith('hi') ? 'hi' :
   navigator.language.startsWith('de') ? 'de' :
   navigator.language.startsWith('fr') ? 'fr' :
   navigator.language.startsWith('it') ? 'it' :
   navigator.language.startsWith('es') ? 'es' :
   navigator.language.startsWith('ar') ? 'ar' : 'en');

function t(key) {
  return I18N[currentLang]?.[key] || I18N['en']?.[key] || key;
}

function setLang(lang) {
  if (!I18N[lang]) return;
  currentLang = lang;
  localStorage.setItem('lang', lang);
  const meta = LANG_META[lang];
  document.documentElement.lang = lang;
  document.documentElement.dir = meta.dir;
  applyI18n();
  renderLangSwitcher('langSwitcher');
  renderLangSwitcher('mobileLangSwitcher');
}

function applyI18n() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.title = t(el.dataset.i18nTitle);
  });
}

function renderLangSwitcher(containerId) {
  const container = document.getElementById(containerId);
  if (!container) return;
  container.innerHTML = Object.entries(LANG_META).map(([code, meta]) =>
    `<button onclick="setLang('${code}')" title="${meta.name}"
      style="background:none;border:none;cursor:pointer;font-size:1.2rem;padding:2px 4px;opacity:${currentLang===code?'1':'0.5'};transition:opacity 0.2s;"
      onmouseover="this.style.opacity='1'" onmouseout="this.style.opacity='${currentLang===code?'1':'0.5'}'">
      ${meta.flag}
    </button>`
  ).join('');
}

document.addEventListener('DOMContentLoaded', () => {
  applyI18n();
  renderLangSwitcher('langSwitcher');
  renderLangSwitcher('mobileLangSwitcher');
});
