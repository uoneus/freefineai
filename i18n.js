let currentLang = localStorage.getItem('lang') ||
  (navigator.language.startsWith('zh')?'zh':
   navigator.language.startsWith('ru')?'ru':
   navigator.language.startsWith('ja')?'ja':
   navigator.language.startsWith('hi')?'hi':
   navigator.language.startsWith('de')?'de':
   navigator.language.startsWith('fr')?'fr':
   navigator.language.startsWith('it')?'it':
   navigator.language.startsWith('es')?'es':
   navigator.language.startsWith('ar')?'ar':'en');

function t(key){return I18N[currentLang]?.[key]||I18N['en']?.[key]||key;}

function setLang(lang){
  if(!I18N[lang])return;
  currentLang=lang;
  localStorage.setItem('lang',lang);
  document.documentElement.lang=lang;
  document.documentElement.dir=LANG_META[lang].dir;
  applyI18n();
  renderLangSwitcher('langSwitcher');
  if(document.getElementById('mobileLangSwitcher'))renderLangSwitcher('mobileLangSwitcher');
}

function applyI18n(){
  document.querySelectorAll('[data-i18n]').forEach(el=>{el.textContent=t(el.dataset.i18n);});
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el=>{el.placeholder=t(el.dataset.i18nPlaceholder);});
}

function renderLangSwitcher(containerId){
  const c=document.getElementById(containerId);
  if(!c)return;
  const sel=document.createElement('select');
  sel.style.cssText='background:#1a1a2e;color:#e4e4e7;border:1px solid rgba(255,255,255,0.15);border-radius:8px;padding:4px 8px;font-size:0.8rem;cursor:pointer;outline:none;';
  sel.onchange=function(){setLang(this.value);};
  Object.entries(LANG_META).forEach(([code,meta])=>{
    const opt=document.createElement('option');
    opt.value=code;
    opt.textContent=meta.flag+' '+meta.name;
    if(code===currentLang)opt.selected=true;
    sel.appendChild(opt);
  });
  c.innerHTML='';
  c.appendChild(sel);
}

document.addEventListener('DOMContentLoaded',()=>{
  applyI18n();
  renderLangSwitcher('langSwitcher');
  if(document.getElementById('mobileLangSwitcher'))renderLangSwitcher('mobileLangSwitcher');
});
